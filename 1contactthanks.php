<p class="title">MAIN FEATURES</p> <br />
<p class="subtitle"><strong>RADIO</strong></p>
<p style="font-size: 16px">Beautiful Clock Radio integrates the best internet radio (powered by Shoutcast) with access to over 35,000 music, sports, talk and community programmed stations from around the globe. The application can play music stored on your iPad, 1 of 13 soothing sounds to help you relax, tune into the TWIT Network or any mp3 stream. All can be saved to one of 5 preset buttons. </p>
<p class="subtitle"><strong>ANIMATED BACKGROUND</strong></p>
<p style="font-size: 16px">The background changes to reflect the time of day. At 12pm the sun is high and slowly sets to accurately reflect the sunset in your current location. <br />

At night the moon is shown in the correct phase. </p>
<p class="subtitle"><strong>ALARM</strong></p>
<p style="font-size: 16px">Beautiful Clock Radio features an innovative and easy to set alarm. Simply touch the alarm face to set the time you want. Selecting the AM or PM finalizes and sets the alarm. </p>
